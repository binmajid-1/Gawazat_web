    echo sprintf( __( 'User first name: %s,<br />', 'textdomain' ), esc_html( $current_user->user_firstname ) );
    echo sprintf( __( 'User last name: %s,<br />', 'textdomain' ), esc_html( $current_user->user_lastname ) );
    echo sprintf( __( 'User display name: %s,<br />', 'textdomain' ), esc_html( $current_user->display_name ) );
    echo sprintf( __( 'User ID: %s', 'textdomain' ), esc_html( $current_user->ID ) );
    }
    


